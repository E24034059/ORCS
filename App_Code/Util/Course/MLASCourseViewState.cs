        }

        /// <summary>
        /// State: ViewCourse  只有該課程的學生可以觀看
        /// </summary>
        public static string ViewCourse
        {
            get { return "ViewCourse"; }
        }
        /// <summary>
        /// State: HindCourse  所有學生皆看不到課程
        /// </summary>
        public static string HindCourse
        {
            get { return "HindCourse"; }
        }
        /// <summary>
        /// State: PublicCourse 所有學生皆可看到課程
        /// </summary>
        public static string PublicCourse
        {
            get { return "PublicCourse"; }
        }
    }
